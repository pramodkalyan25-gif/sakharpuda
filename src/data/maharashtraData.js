export const HEIGHTS_CM = (() => {
  const list = [];
  for (let cm = 134; cm <= 210; cm++) {
    const totalInches = cm / 2.54;
    const ft = Math.floor(totalInches / 12);
    const inch = Math.round(totalInches % 12);
    list.push({ value: cm, label: `${ft}ft ${inch}in - ${cm}cm` });
  }
  return list;
})();
